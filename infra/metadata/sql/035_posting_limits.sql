create table if not exists public.posting_policy_tiers (
  id bigint generated by default as identity primary key,
  tier_name text not null unique,
  min_account_age_days integer not null default 0
    check (min_account_age_days >= 0),
  thread_limit_per_day integer not null
    check (thread_limit_per_day >= 0),
  reply_limit_per_day integer not null
    check (reply_limit_per_day >= 0),
  created_at timestamp with time zone not null default timezone('utc', now()),
  updated_at timestamp with time zone not null default timezone('utc', now())
);

create table if not exists public.posting_policy_activity_requirements (
  tier_id bigint primary key references public.posting_policy_tiers(id) on delete cascade,
  lookback_days integer not null
    check (lookback_days > 0),
  min_active_days integer not null
    check (min_active_days >= 0 and min_active_days <= lookback_days),
  created_at timestamp with time zone not null default timezone('utc', now()),
  updated_at timestamp with time zone not null default timezone('utc', now())
);

create table if not exists public.account_moderation_flags (
  user_id uuid primary key references public.profiles(id) on delete cascade,
  is_posting_blocked boolean not null default true,
  reason text,
  flagged_by uuid references public.profiles(id) on delete set null,
  created_at timestamp with time zone not null default timezone('utc', now()),
  updated_at timestamp with time zone not null default timezone('utc', now())
);

create table if not exists public.account_daily_post_stats (
  user_id uuid not null references public.profiles(id) on delete cascade,
  activity_date date not null,
  thread_count integer not null default 0
    check (thread_count >= 0),
  reply_count integer not null default 0
    check (reply_count >= 0),
  created_at timestamp with time zone not null default timezone('utc', now()),
  updated_at timestamp with time zone not null default timezone('utc', now()),
  primary key (user_id, activity_date)
);

create index if not exists posting_policy_tiers_min_age_idx
  on public.posting_policy_tiers (min_account_age_days desc);

create index if not exists account_daily_post_stats_activity_date_idx
  on public.account_daily_post_stats (activity_date desc);

