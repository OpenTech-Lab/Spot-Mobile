insert into public.account_daily_post_stats (
  user_id,
  activity_date,
  thread_count,
  reply_count,
  created_at,
  updated_at
)
select
  p.user_id,
  timezone('utc', p.created_at)::date as activity_date,
  count(*) filter (where p.reply_to_id is null)::integer as thread_count,
  count(*) filter (where p.reply_to_id is not null)::integer as reply_count,
  min(p.created_at) as created_at,
  timezone('utc', now()) as updated_at
from public.posts p
group by p.user_id, timezone('utc', p.created_at)::date
on conflict (user_id, activity_date) do update
  set thread_count = excluded.thread_count,
      reply_count = excluded.reply_count,
      updated_at = timezone('utc', now());

